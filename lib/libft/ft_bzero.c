#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ps;
	size_t	i;

	if (n == 0)
		return ;
	ps = (char *)s;
	i = 0;
	while (i < n)
		ps[i++] = 0;
}
